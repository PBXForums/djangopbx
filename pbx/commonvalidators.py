import re

def valid_uuid4(t_uuid):
    regex = re.compile('[0-9A-F]{8}-[0-9A-F]{4}-[4][0-9A-F]{3}-[89AB][0-9A-F]{3}-[0-9A-F]{12}', re.I)
    match = regex.match(t_uuid)
    return bool(match)

def clean_uuid4_list(t_list):
        for t_uuid in t_list:
            if not valid_uuid4(t_uuid):
                t_list.remove(t_uuid)
